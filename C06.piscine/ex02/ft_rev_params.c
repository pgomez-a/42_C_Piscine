#include <unistd.h>

int		main(int argc, char **argv)
{
	int		i;
	int		t;

	if (argc > 1)
	{
		i = 1;
		while (argv[i] != '\0')
			i++;
		i--;
		while (i >= 1)
		{
			t = 0;
			while (argv[i][t] != '\0')
			{
				write(1, &argv[i][t], 1);
				t++;
			}
			write(1, "\n", 1);
			i--;
		}
	}
}
