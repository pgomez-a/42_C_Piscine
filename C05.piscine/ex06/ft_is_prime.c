int		ft_is_prime(int nb)
{
	int		count;

	count = 2;
	if (nb < 0 || nb == 1 || nb == 0)
		return (0);
	if (nb == 3)
		return (1);
	while (count < nb)
	{
		if (nb % count == 0)
			return (0);
		count++;
	}
	return (1);
}
